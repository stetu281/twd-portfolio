<h2 class="wp-block-heading">Wie ich dazu kam</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Im Jahr 2020, mitten in der Corona Zeit, verbrachte ich einige Monate in Portugal. Während dieser Zeit kam mir die Idee, von Portugal zurüpck in die Schweiz zu laufen. Der Jakobsweg war mir zwar ein Begriff, jedoch wusste ich noch  nicht viel darüber. Also begann ich mich ein wenig mit dem Thema zu beschäftigen, indem ich einige Blogs las und  Videos anschaute. Die Geschichten und Erfahrungen anderer Pilger faszinierten mich und weckten den Wunsch, diese Reise selber zu erleben.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Trotz meiner Begeisterung entschied ich mich letztentlich dagegen, den Weg zurückzulaufen. Der Lehrgang zum Web Entwickler begann bald und ich wollte den Start nicht verpassen.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">Der erste "Testlauf"</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Zwei Jahre später konnte ich mich nicht entscheiden, was ich während meinen Sommerferien machen wollte. Da erinnerte ich mich wieder an den Jakobsweg. Da ich ins Ausland wollte, entschied ich mich mal drei bis vier Tage von Le Puy en Velay auf dem Jakobsweg in Frankreich zu laufen, so quasi als Test, ob es mir überhaupt Spass macht. Ich machte ca. 100Km in 3.5 Tagen und hatte sehr viel Spass dabei.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading">Warum laufe ich auf dem Jakobsweg</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Schon immer habe ich es geliebt, unterwegs zu sein und neue Orte zu entdecken, sei es zu Fuss oder mit dem Auto auf einem Roadtrip. Wie bei vielen anderen, die ich unterwegs getroffen habe, hat es für mich keinen religiösen Hintergrund. Für mich hat es etwas befreiendes, bei dem ich meinen Alltag hinter mir lassen kann. Es ist kein Rundweg, bei dem ich am Ende wieder am Ausgangspunkt ankomme. Stattdessen gehe ich immer weiter und lasse dabei die Ortschaften und den Startpunkt hinter mir. Dabei kann ich meine Gedanken schweifen lassen oder auch mal komplett abschalten und einfach nur die Aussichten und die Natur geniessen und im Moment leben.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">Was ich noch machen will</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Natürlich möchte ich schlussendlich den ganzen Weg gemacht haben ;-) Bis dahin ist jedoch noch ein weiter Weg. Da ich nicht den kompletten Weg in einmal mache, werde ich weiterhin Teilstücke davon machen und sehen ob ich es jemals bis Santiago de Compostela schaffe. </p>
